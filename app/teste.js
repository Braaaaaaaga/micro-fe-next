// Erros de Linting Intencionais

const foo = "bar"

// Variável não utilizada
const unusedVar = 42

// Indentação incorreta
function helloWorld() {
console.log("Hello, world!");
}

// Falta de ponto e vírgula
const greet = () => {
  return "Hello"
}

// Uso de 'var' em vez de 'let' ou 'const'
var name = "John Doe"

// Falta de espaço antes do comentário
const sum = (a,b) => {
  return a + b
}

// Nomes de funções não camelCase
function my_function() {
  console.log("This should be camelCase");
}
